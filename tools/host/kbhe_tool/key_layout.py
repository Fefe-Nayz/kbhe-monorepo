KEY_LAYOUT_HEIGHT = max(entry.y + entry.h for entry in KEY_LAYOUT)


def key_label(index: int) -> str:
    if 0 <= index < len(KEY_LAYOUT):
        return KEY_LAYOUT[index].label
    return f"Key {index + 1}"


def key_short_label(index: int) -> str:
    if 0 <= index < len(KEY_LAYOUT):
        return KEY_LAYOUT[index].short_label
    return f"K{index + 1}"


def key_display_name(index: int) -> str:
    return f"K{index + 1:02d} Â· {key_label(index)}"
